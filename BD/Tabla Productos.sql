-- Create table
create table PRODUCTOS
(
  id          NUMBER,
  nombre      VARCHAR2(100),
  descripcion NVARCHAR2(100),
  marca       VARCHAR2(100),
  precio      NUMBER,
  stock       NUMBER
)
tablespace SYSTEM
  pctfree 10
  pctused 40
  initrans 1
  maxtrans 255
  storage
  (
    initial 64K
    next 1M
    minextents 1
    maxextents unlimited
  );
-- Create/Recreate indexes 
create index IDX_IDPRODUCTO on PRODUCTOS (ID)
  tablespace SYSTEM
  pctfree 10
  initrans 2
  maxtrans 255
  storage
  (
    initial 64K
    next 1M
    minextents 1
    maxextents unlimited
  );
